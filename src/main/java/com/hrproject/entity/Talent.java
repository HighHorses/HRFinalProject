package com.hrproject.entity;

public class Talent {

  private long id;
  private String fpsgName;
  private String fpsgSex;
  private String phone;
  private String xueli;
  private String zhuanye;
  private String experience;
  private long whether;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getFpsgName() {
    return fpsgName;
  }

  public void setFpsgName(String fpsgName) {
    this.fpsgName = fpsgName;
  }


  public String getFpsgSex() {
    return fpsgSex;
  }

  public void setFpsgSex(String fpsgSex) {
    this.fpsgSex = fpsgSex;
  }


  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }


  public String getXueli() {
    return xueli;
  }

  public void setXueli(String xueli) {
    this.xueli = xueli;
  }


  public String getZhuanye() {
    return zhuanye;
  }

  public void setZhuanye(String zhuanye) {
    this.zhuanye = zhuanye;
  }


  public String getExperience() {
    return experience;
  }

  public void setExperience(String experience) {
    this.experience = experience;
  }


  public long getWhether() {
    return whether;
  }

  public void setWhether(long whether) {
    this.whether = whether;
  }

}
