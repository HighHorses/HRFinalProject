package com.hrproject.entity;

public class Zhiwei {

  private long id;
  private String zwNo;
  private String zwName;
  private long zwbm;
  private long zwtype;
  private String describEs;
  private String remark;
  private long startUsing;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getZwNo() {
    return zwNo;
  }

  public void setZwNo(String zwNo) {
    this.zwNo = zwNo;
  }


  public String getZwName() {
    return zwName;
  }

  public void setZwName(String zwName) {
    this.zwName = zwName;
  }


  public long getZwbm() {
    return zwbm;
  }

  public void setZwbm(long zwbm) {
    this.zwbm = zwbm;
  }


  public long getZwtype() {
    return zwtype;
  }

  public void setZwtype(long zwtype) {
    this.zwtype = zwtype;
  }


  public String getDescribEs() {
    return describEs;
  }

  public void setDescribEs(String describEs) {
    this.describEs = describEs;
  }


  public String getRemark() {
    return remark;
  }

  public void setRemark(String remark) {
    this.remark = remark;
  }


  public long getStartUsing() {
    return startUsing;
  }

  public void setStartUsing(long startUsing) {
    this.startUsing = startUsing;
  }

}
